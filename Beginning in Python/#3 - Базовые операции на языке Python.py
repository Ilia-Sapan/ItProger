print("World!")
print("Hello \" World!") # символ \ делает из кавычки обычный символ, который выводится, либо...
print('Hello " World') # Одинарные кавычки отменяют двойные. Двойная не будет закрывающей
print("Hello \t World") # \t символ табуляции, TAB, большой пробел
# ** возведение в степень, // целочисленное деление, округляет
print('Минимальный элемент: ', min(4, 5, 6, 12, 54, 36)) # функция min() выдает минимальный элемент
print('Максимальный элемент: ', max(12, 434, 3223, 323, -4343, 21121)) # функция max() максимальный элемент
print('Число по модулю: ', abs(-5)) # функция abs() выдает число по модулю
print('Число в степени', pow(5, 3)) # функция возведения числа в степень pow (x,y) где x - число, y - степень
print('Округление', round(5 / 3)) # функция round() округляет число к ближайшему

