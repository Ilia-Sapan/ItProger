# 2Ô∏è‚É£ –î–ª—è –∫–∞–∂–¥–æ–π –æ—Å—Ç–∞–≤—à–µ–π—Å—è —Ç–æ—á–∫–∏ –≤—ã—á–∏—Å–ª—è–µ–º —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ –¥–æ –±–ª–∏–∂–∞–π—à–µ–≥–æ —É–∂–µ –≤—ã–±—Ä–∞–Ω–Ω–æ–≥–æ —Ü–µ–Ω—Ç—Ä–æ–∏–¥–∞.
# 3Ô∏è‚É£ –ß–µ–º –¥–∞–ª—å—à–µ —Ç–æ—á–∫–∞ –æ—Ç —É–∂–µ –≤—ã–±—Ä–∞–Ω–Ω—ã—Ö —Ü–µ–Ω—Ç—Ä–æ–∏–¥–æ–≤, —Ç–µ–º –≤—ã—à–µ —à–∞–Ω—Å –µ–µ –≤—ã–±—Ä–∞—Ç—å –≤ –∫–∞—á–µ—Å—Ç–≤–µ –Ω–æ–≤–æ–≥–æ —Ü–µ–Ω—Ç—Ä–æ–∏–¥–∞.
# 4Ô∏è‚É£ –ü–æ–≤—Ç–æ—Ä—è–µ–º —à–∞–≥–∏ 2‚Äì3, –ø–æ–∫–∞ –Ω–µ –≤—ã–±—Ä–∞–Ω–æ K —Ü–µ–Ω—Ç—Ä–æ–∏–¥–æ–≤.
# 5Ô∏è‚É£ –ó–∞—Ç–µ–º –≤—ã–ø–æ–ª–Ω—è–µ–º —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π –∞–ª–≥–æ—Ä–∏—Ç–º K-Means.
#
# üîπ –í —á–µ–º –ø—Ä–µ–∏–º—É—â–µ—Å—Ç–≤–∞ K-Means++?
# ‚úÖ –ë–æ–ª–µ–µ —Å—Ç–∞–±–∏–ª—å–Ω—ã–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã ‚Äì —Å–Ω–∏–∂–∞–µ—Ç –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å –ø–ª–æ—Ö–æ–≥–æ —Ä–∞–∑–±–∏–µ–Ω–∏—è.
# ‚úÖ –ë–æ–ª–µ–µ –±—ã—Å—Ç—Ä–∞—è —Å—Ö–æ–¥–∏–º–æ—Å—Ç—å ‚Äì —Ç—Ä–µ–±—É–µ—Ç –º–µ–Ω—å—à–µ –∏—Ç–µ—Ä–∞—Ü–∏–π, —á–µ–º –æ–±—ã—á–Ω—ã–π K-Means.
# ‚úÖ –£–ª—É—á—à–∞–µ—Ç –∫–∞—á–µ—Å—Ç–≤–æ –∫–ª–∞—Å—Ç–µ—Ä–æ–≤ ‚Äì –Ω–∞—á–∞–ª—å–Ω—ã–µ —Ü–µ–Ω—Ç—Ä–æ–∏–¥—ã —Ä–∞—Å–ø–æ–ª–æ–∂–µ–Ω—ã —Ä–∞–∑—É–º–Ω–µ–µ.
#
import numpy as np
import matplotlib.pyplot as plt
from sklearn.cluster import KMeans
from sklearn.metrics import silhouette_score

# –î–∞–Ω–Ω—ã–µ –¥–ª—è –∫–ª–∞—Å—Ç–µ—Ä–∏–∑–∞—Ü–∏–∏
x = np.array([4, 5, 10, 4, 3, 11, 14, 6, 10, 12])
y = np.array([21, 19, 24, 17, 16, 25, 24, 22, 21, 21])

# –û–±—ä–µ–¥–∏–Ω—è–µ–º x –∏ y –≤ –¥–≤—É–º–µ—Ä–Ω—ã–π –º–∞—Å—Å–∏–≤ (–º–∞—Ç—Ä–∏—Ü—É –ø—Ä–∏–∑–Ω–∞–∫–æ–≤)
data = np.column_stack((x, y))

# –°–ø–∏—Å–æ–∫ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —Å–∏–ª—É—ç—Ç–Ω—ã—Ö –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç–æ–≤
silhouette_coefficients = []

# –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∫–ª–∞—Å—Ç–µ—Ä–æ–≤ –æ—Ç 2 –¥–æ 8
for k in range(2, 9):
    km = KMeans(n_clusters=k, init="k-means++", random_state=42)  # –ò—Å–ø–æ–ª—å–∑—É–µ–º K-Means++
    km.fit(data)

    score = silhouette_score(data, km.labels_)  # –í—ã—á–∏—Å–ª—è–µ–º —Å–∏–ª—É—ç—Ç–Ω—ã–π –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç
    silhouette_coefficients.append(score)

# –°—Ç—Ä–æ–∏–º –≥—Ä–∞—Ñ–∏–∫ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ —Å–∏–ª—É—ç—Ç–Ω–æ–≥–æ –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç–∞ –æ—Ç K
plt.plot(range(2, 9), silhouette_coefficients, marker="o", linestyle="-")
plt.xlabel("–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –∫–ª–∞—Å—Ç–µ—Ä–æ–≤ (K)")
plt.ylabel("–°–∏–ª—É—ç—Ç–Ω—ã–π –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç")
plt.title("–í—ã–±–æ—Ä –æ–ø—Ç–∏–º–∞–ª—å–Ω–æ–≥–æ K —Å –ø–æ–º–æ—â—å—é —Å–∏–ª—É—ç—Ç–Ω–æ–≥–æ –º–µ—Ç–æ–¥–∞")
plt.show()






