# üìå –ò—Ç–æ–≥:
# K-Means ‚Äì –æ–¥–∏–Ω –∏–∑ –±–∞–∑–æ–≤—ã—Ö –∏ –ø–æ–ø—É–ª—è—Ä–Ω—ã—Ö –º–µ—Ç–æ–¥–æ–≤ –∫–ª–∞—Å—Ç–µ—Ä–∏–∑–∞—Ü–∏–∏,
# –Ω–æ –¥–ª—è —Å–ª–æ–∂–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö –º–æ–≥—É—Ç –ø–æ—Ç—Ä–µ–±–æ–≤–∞—Ç—å—Å—è –±–æ–ª–µ–µ –ø—Ä–æ–¥–≤–∏–Ω—É—Ç—ã–µ –∞–ª–≥–æ—Ä–∏—Ç–º—ã
# (DBSCAN, –∞–≥–ª–æ–º–µ—Ä–∞—Ç–∏–≤–Ω–∞—è –∫–ª–∞—Å—Ç–µ—Ä–∏–∑–∞—Ü–∏—è –∏ –¥—Ä.).
#
# üìå –†–∞–∑–Ω–∏—Ü–∞ –º–µ–∂–¥—É K-Means –∏ K-Means++
# üîπ –°—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π K-Means –≤—ã–±–∏—Ä–∞–µ—Ç K –Ω–∞—á–∞–ª—å–Ω—ã—Ö —Ü–µ–Ω—Ç—Ä–æ–∏–¥–æ–≤ —Å–ª—É—á–∞–π–Ω–æ, —á—Ç–æ –º–æ–∂–µ—Ç –ø—Ä–∏–≤–æ–¥–∏—Ç—å –∫ –ø–ª–æ—Ö–æ–π –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏ –∏ –Ω–µ—É—Å—Ç–æ–π—á–∏–≤—ã–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞–º.
# üîπ K-Means++ —É–ª—É—á—à–∞–µ—Ç —ç—Ç–æ—Ç –ø—Ä–æ—Ü–µ—Å—Å, –≤—ã–±–∏—Ä–∞—è –Ω–∞—á–∞–ª—å–Ω—ã–µ —Ü–µ–Ω—Ç—Ä–æ–∏–¥—ã –±–æ–ª–µ–µ –æ—Å–º—ã—Å–ª–µ–Ω–Ω–æ, —á—Ç–æ –ø–æ–≤—ã—à–∞–µ—Ç —Ç–æ—á–Ω–æ—Å—Ç—å –∏ —Å–∫–æ—Ä–æ—Å—Ç—å —Å—Ö–æ–¥–∏–º–æ—Å—Ç–∏.
#
# üîπ –ö–∞–∫ —Ä–∞–±–æ—Ç–∞–µ—Ç K-Means++?
