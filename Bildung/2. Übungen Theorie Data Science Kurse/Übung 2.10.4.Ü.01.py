from scipy.spatial import distance_matrix

# Пусть у нас есть пять точек на числовой прямой
y = [[10], [7], [28], [20], [35]]

# distance_matrix(X, Y) → матрица расстояний между каждой точкой из X и каждой из Y
dist = distance_matrix(y, y)

print(dist)

# [[ 0.  3. 18. 10. 25.]
#  [ 3.  0. 21. 13. 28.]
#  [18. 21.  0.  8.  7.]
#  [10. 13.  8.  0. 15.]
#  [25. 28.  7. 15.  0.]]