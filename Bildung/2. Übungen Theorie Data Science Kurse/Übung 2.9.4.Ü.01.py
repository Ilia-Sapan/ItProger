# Импортируем библиотеки для работы с кластеризацией и визуализацией
import matplotlib.pyplot as plt
from sklearn.cluster import KMeans

# Определяем данные: координаты x и y
x = [4, 5, 10, 4, 3, 11, 14, 6, 10, 12]
y = [21, 19, 24, 17, 16, 25, 24, 22, 21, 21]

# Создаём список кортежей (объединяем x и y в пары координат)
data = list(zip(x, y))
print(data)  # Вывод данных в консоль

# Создаём модель K-Means с 2 кластерами
kmeans2 = KMeans(n_clusters=2)
kmeans2.fit(data)  # Обучаем модель на данных

# Визуализируем результаты кластеризации
plt.scatter(x, y, c=kmeans2.labels_)  # Цвет точек зависит от метки кластера
plt.show()  # Отображаем график



