

# Список, в котором будем искать элемент
liste = [10, 19, 23, 25, 36, 61, 79, 81, 99]

# Функция для бинарного поиска
def binsuche(x, daten):
    # Устанавливаем верхнюю и нижнюю границу диапазона для поиска
    high = len(daten) - 1  # Индекс последнего элемента списка
    low = 0  # Индекс первого элемента списка

    # Пока диапазон поиска не сужен до одного элемента
    while low <= high:
        # Находим средний элемент
        mid = low + (high - low) // 2

        # Если элемент найден, возвращаем его индекс
        if daten[mid] == x:
            return mid
        # Если элемент больше среднего, сужаем диапазон поиска до правой половины
        elif daten[mid] < x:
            low = mid + 1
        # Если элемент меньше среднего, сужаем диапазон поиска до левой половины
        else:
            high = mid - 1

    # Если элемент не найден, возвращаем -1
    return -1

# Ищем число 19 в списке и выводим результат
print(binsuche(19, liste))
