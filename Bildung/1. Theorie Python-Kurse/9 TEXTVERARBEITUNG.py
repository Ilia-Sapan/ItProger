# –ì—Ä—É–ø–ø—ã –∏ –∞–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤—ã
#
# [abc] ‚Äì –ª—é–±–æ–π –∏–∑ —Å–∏–º–≤–æ–ª–æ–≤ a, b, c
# [^abc] ‚Äì –ª—é–±–æ–π —Å–∏–º–≤–æ–ª, –∫—Ä–æ–º–µ a, b, c
# (abc|def) ‚Äì –ª–∏–±–æ abc, –ª–∏–±–æ def
# (group) ‚Äì –≥—Ä—É–ø–ø–∏—Ä–æ–≤–∫–∞
# –Ø–∫–æ—Ä—è (—É–∫–∞–∑—ã–≤–∞—é—Ç –Ω–∞ –ø–æ–∑–∏—Ü–∏—é –≤ —Å—Ç—Ä–æ–∫–µ)
#
# ^ ‚Äì –Ω–∞—á–∞–ª–æ —Å—Ç—Ä–æ–∫–∏
# $ ‚Äì –∫–æ–Ω–µ—Ü —Å—Ç—Ä–æ–∫–∏
# \b ‚Äì –≥—Ä–∞–Ω–∏—Ü–∞ —Å–ª–æ–≤–∞
#
# –í–æ—Ç —Å–ø–∏—Å–æ–∫ —Å–ø–µ—Ü–∏–∞–ª—å–Ω—ã—Ö —Å–∏–º–≤–æ–ª–æ–≤ (Sonderzeichen) —Ä–µ–≥—É–ª—è—Ä–Ω—ã—Ö –≤—ã—Ä–∞–∂–µ–Ω–∏–π —Å –∫—Ä–∞—Ç–∫–∏–º –æ–ø–∏—Å–∞–Ω–∏–µ–º:
#
# üîπ –ö–≤–∞–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä—ã (–ø–æ–≤—Ç–æ—Ä—ã)
# –°–∏–º–≤–æ–ª	–û–ø–∏—Å–∞–Ω–∏–µ
# *	0 –∏–ª–∏ –±–æ–ª–µ–µ –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π
# +	1 –∏–ª–∏ –±–æ–ª–µ–µ –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π
# ?	0 –∏–ª–∏ 1 –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–µ
# {n}	–†–æ–≤–Ω–æ n –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π
# {n,}	n –∏ –±–æ–ª–µ–µ –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π
# {n,m}	–û—Ç n –¥–æ m –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π
# *?	–õ–µ–Ω–∏–≤—ã–π * (–º–∏–Ω–∏–º–∞–ª—å–Ω–æ –≤–æ–∑–º–æ–∂–Ω–æ–µ —á–∏—Å–ª–æ –ø–æ–≤—Ç–æ—Ä–æ–≤)
