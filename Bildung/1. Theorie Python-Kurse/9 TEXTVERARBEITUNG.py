# \S	–ù–µ –ø—Ä–æ–±–µ–ª
# \w	–ë—É–∫–≤–∞, —Ü–∏—Ñ—Ä–∞ –∏–ª–∏ _ (a-z, A-Z, 0-9, _)
# \W	–ù–µ –±—É–∫–≤–∞, —Ü–∏—Ñ—Ä–∞ –∏–ª–∏ _ (–Ω–∞–ø—Ä–∏–º–µ—Ä, @, !, #)
# üîπ –Ø–∫–æ—Ä—è (anchors)
# –°–∏–º–≤–æ–ª	–û–ø–∏—Å–∞–Ω–∏–µ
# ^	–ù–∞—á–∞–ª–æ —Å—Ç—Ä–æ–∫–∏
# $	–ö–æ–Ω–µ—Ü —Å—Ç—Ä–æ–∫–∏
# \b	–ì—Ä–∞–Ω–∏—Ü–∞ —Å–ª–æ–≤–∞
# \B	–ù–µ –≥—Ä–∞–Ω–∏—Ü–∞ —Å–ª–æ–≤–∞
# \Z	–ö–æ–Ω–µ—Ü —Å—Ç—Ä–æ–∫–∏ –∏–ª–∏ —Ñ–∞–π–ª–∞
