# (abc)	–ì—Ä—É–ø–ø–∞ (–∑–∞–ø–æ–º–∏–Ω–∞–µ—Ç —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ)
# (?:abc)	–ù–µ–≥—Ä—É–ø–ø–∏—Ä—É—é—â–∏–µ —Å–∫–æ–±–∫–∏ (–Ω–µ –∑–∞–ø–æ–º–∏–Ω–∞–µ—Ç)
# (?=abc)	–ü–æ–∑–∏—Ç–∏–≤–Ω—ã–π lookahead (–ø—Ä–æ–≤–µ—Ä—è–µ—Ç, —á—Ç–æ –¥–∞–ª—å—à–µ abc, –Ω–æ –Ω–µ –≤–∫–ª—é—á–∞–µ—Ç –µ–≥–æ)
# (?!abc)	–ù–µ–≥–∞—Ç–∏–≤–Ω—ã–π lookahead (—É–±–µ–∂–¥–∞–µ—Ç—Å—è, —á—Ç–æ –¥–∞–ª—å—à–µ –Ω–µ abc)
# (?<=abc)	–ü–æ–∑–∏—Ç–∏–≤–Ω—ã–π lookbehind (–ø—Ä–æ–≤–µ—Ä—è–µ—Ç, —á—Ç–æ –ø–µ—Ä–µ–¥ –µ—Å—Ç—å abc)
