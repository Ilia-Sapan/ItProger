# \w ‚Äì –ª—é–±–∞—è –±—É–∫–≤–∞ –∏–ª–∏ —Ü–∏—Ñ—Ä–∞ (a-z, A-Z, 0-9, _)
# \s ‚Äì –ª—é–±–æ–π –ø—Ä–æ–±–µ–ª—å–Ω—ã–π —Å–∏–º–≤–æ–ª (–ø—Ä–æ–±–µ–ª, —Ç–∞–±—É–ª—è—Ü–∏—è, –ø–µ—Ä–µ–Ω–æ—Å —Å—Ç—Ä–æ–∫–∏)
# –ö–≤–∞–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä—ã (—É–∫–∞–∑—ã–≤–∞—é—Ç, —Å–∫–æ–ª—å–∫–æ —Ä–∞–∑ –ø–æ–≤—Ç–æ—Ä—è–µ—Ç—Å—è —ç–ª–µ–º–µ–Ω—Ç)
#
# * ‚Äì 0 –∏–ª–∏ –±–æ–ª–µ–µ —Ä–∞–∑
# + ‚Äì 1 –∏–ª–∏ –±–æ–ª–µ–µ —Ä–∞–∑
# ? ‚Äì 0 –∏–ª–∏ 1 —Ä–∞–∑
# {n} ‚Äì —Ä–æ–≤–Ω–æ n —Ä–∞–∑
# {n,} ‚Äì n –∏ –±–æ–ª–µ–µ —Ä–∞–∑
